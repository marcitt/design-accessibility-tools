
import mss
from PIL import Image, ImageGrab
import pyautogui

import time

# Screenshots with PIL is very simple
def screenshot_pil(output_path="screenshot.png"):
    img = ImageGrab.grab()
    img.save(output_path)
    return img

# Screenshots with pyautogui is also very simple
# However this is just a wrapper over PIL 
def screenshot_pyautogui(output_path="screenshot.png"):
    img = pyautogui.screenshot()
    img.save(output_path)
    return img

# Screenshots with MSS

def screenshot_mss(output_path="screenshot.png"):
    with mss.mss() as sct:
        sct_img = sct.grab(sct.monitors[1]) #allows you to choose the monitor
    img = Image.frombytes("RGB", sct_img.size, sct_img.bgra, "raw", "BGRX")
    img.save(output_path)
    return img


def screenshot_mss_region(x, y, w, h, output_path="screenshot_region.png"):
    with mss.mss() as sct:
        monitor = {"top": y, "left": x, "width": w, "height": h}
        sct_img = sct.grab(monitor)
    img = Image.frombytes("RGB", sct_img.size, sct_img.bgra, "raw", "BGRX")
    img.save(output_path)
    return img

