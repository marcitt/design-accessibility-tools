# this function was generated by claude to benchmark different screenshots
def benchmark(runs=5):
    methods = {
        "mss":        screenshot_mss,
        "PIL":        screenshot_pil,
        "pyautogui":  screenshot_pyautogui,
    }
    results = {}
    
    for name, fn in methods.items():
        times = []
        for _ in range(runs):
            t0 = time.perf_counter()
            try:
                fn()
            except Exception as e:
                print(f"{name} failed: {e}")
                break
            times.append(time.perf_counter() - t0)
        if times:
            avg = sum(times) / len(times)
            results[name] = round(avg * 1000, 2)
            print(f"{name:12} avg: {avg*1000:.1f}ms")
    return results
    

if __name__ == "__main__":
    print(f"benchmarking...\n")
    benchmark()